// Удаление блоков «рассуждений» (reasoning/chain-of-thought) из ответа LLM.
// Некоторые модели (MiniMax-M3, DeepSeek-R1, Qwen-QwQ) оборачивают ход мыслей в
// специальные теги. Для финального саммари эти блоки — шум: пользователь хочет
// видеть только итоговый Markdown. Здесь — устойчивый парсер, поддерживающий
// несколько форматов и tolerant к незакрытым тегам (стрим мог оборваться).

// Теги, которые считаем «рассуждениями». Регистронезависимый матчинг.
// При появлении новых форматов — добавить сюда.
export const REASONING_TAGS = ["think", "reasoning", "reflection"] as const;

// Построить regex для пары открывающий/закрывающий тег.
// `<think\b[^>]*>` — открывающий (с возможными атрибутами, напр. `<think phase="1">`).
// `[\s\S]*?` — ленивый матч содержимого (любые символы, включая переводы строк).
// `<\/think\s*>` — закрывающий.
function closedBlockRe(tag: string): RegExp {
  return new RegExp(`<${tag}\\b[^>]*>[\\s\\S]*?<\\/${tag}\\s*>`, "gi");
}

// Незакрытый блок: открывающий тег есть, закрывающего нет → убрать до конца строки.
// Случай: стрим оборвался, модель не закрыла тег. `[\s\S]*` — жадный, до конца.
function unclosedBlockRe(tag: string): RegExp {
  return new RegExp(`<${tag}\\b[^>]*>[\\s\\S]*$`, "i");
}

// Убрать блоки рассуждений из текста. Возвращает очищенный текст:
//   - все закрытые `<tag>…</tag>` удалены;
//   - незакрытый `<tag>…` (до конца) удалён;
//   - лидирующие/концевые пробелы и пустые строки обрезаны;
//   - схлопнуты 3+ перевода строк (могут остаться после удаления блока) в 2.
//
// Если тегов нет — текст возвращается без изменений (быстрый путь).
export function stripReasoning(text: string): string {
  if (!text) return "";
  // Быстрый путь: если ни один тег не встречается — не гоняем regex.
  const lower = text.toLowerCase();
  const hasAny = REASONING_TAGS.some(
    (tag) => lower.includes(`<${tag}`),
  );
  if (!hasAny) return text;

  let out = text;
  for (const tag of REASONING_TAGS) {
    // Сначала — закрытые блоки (могут быть несколько в одном ответе).
    out = out.replace(closedBlockRe(tag), "");
    // Затем — незакрытый хвост (если остался открывающий без пары).
    out = out.replace(unclosedBlockRe(tag), "");
  }
  // Схлопнуть 3+ перевода строк (остаются после удаления многострочного блока)
  // и обрезать лидирующие/концевые пробелы.
  return out.replace(/\n{3,}/g, "\n\n").trim();
}
